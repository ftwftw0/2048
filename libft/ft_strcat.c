#include "libft.h"

char *ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	char	*tmp;

	i = ft_strlen(s1);
	tmp = s1;
	while (*s2)
		*((s1++) + i) = *(s2++);
	*((s1) + i) = 0;
	return (tmp);
}
