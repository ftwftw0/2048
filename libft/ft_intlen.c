#include "libft.h"

int		ft_intlen(int i)
{
	int j;
	int k;

	k = i / 10;
	j = 1;
	while (k != 0)
	{
		k = k / 10;
		j++;
	}
	return ((i < 0) ? j + 1 : j);
}
